validateAuth($request);
validateNewPassword($request);

require_once PATH_TABLES."user.php";

$user = new UserGateway;
$u = $user->get(Session::get('auth_user_id'));

validatePassword($request, $u['user_password']);

$request->trust();

$user->edit($u["user_id"], array('user_password' => passwordHash($request->param('new_password'))));

$request->ok(lng('password_has_been_changed'));